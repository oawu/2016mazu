    <h2>真人藝閣</h2>

    <p>繞境隊伍中，你不但可以看到具有歷史的傳統陣頭，還可以看到各式的<b>藝閣</b>花車，每輛藝閣都會有個主題故事，而藝閣上也有著很小朋友扮演故事中的角色，那些人物可都是真人的不是一般的電動假人偶！</p>
    <p>藝閣的「藝」指的是<b>南管</b>樂器彈唱的藝旦，而「閣」則指的是架子上臵放食物的木板，兩者合在一起則有為藝術之閣、詩意之閣。</p>
    <p>一般扮演藝閣都會由十四歲以下的童男、童女，著古裝扮演戲劇角色。早期在民國四十年左右，由人工扛抬改為獸力，將「閣棚」裝臵於牛車上，由牛來拉動，後改以機動三輪車帶動，現在藝閣完全改用貨車車體，其外形比當年增大五倍之多！而視覺與藝術美感更勝以往！</p>
    <p>觀賞藝閣時，你只要對這些小朋友招招手，他們就會丟糖果給你！北港的真人藝閣，在台灣已經很少見了，藝閣花車也是行之有年的傳統陣頭之一，藝閣花車從以往的人力抬拉，進而慢慢演進變成用獸力，直到今天的機械動力，藝閣的演進也是一種文化的演進與蛻變，若是你沒看過這麼有特色的在地文化，不仿趁著這今年來北港瞧瞧吧！</p>
  </section>

