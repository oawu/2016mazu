    <h2>吃炮(喫炮)</h2>

    <p>吃炮是個怎麼個吃法？依據北港當地陣頭的習慣最主要會分成三種方式: 1.犁炮 2.踩炮 3.炸轎。</p>
    <p><b>犁炮</b> 顧名思義就是用<b>犁頭生</b>(犁頭鐵質部)搭配<b>火爐</b>施放，當火爐中的木炭將犁頭生燒紅時，就可以用來點燃手中的<b>排炮</b>(尺炮)並在點燃的同時丟向神轎，這種燃放鞭炮的方式稱為犁炮，而後期也有直接使用香來當成引燃的工具。</p>
    <p><b>踩炮</b> 就是用腳踏鞭炮囉！一般會先在馬路上使用<b>竹竿炮</b>(連珠炮)鋪成<b>鞭炮地毯</b>，當神轎經過的同時一並引燃，踏著腳下引燃的鞭炮前進的方式稱為踩炮。</p>
    <p><b>炸轎</b> 當然就是用一大堆的鞭炮來炸媽祖的神轎，這是目前北港吃炮常見的方式，所以也有人說這個就是「吃炮」。</p>
    <p>說到鞭炮文化，推薦北港最猛的吃炮陣頭「<b>虎爺</b>」，虎爺是北港媽祖出巡中最會「喫炮」的神轎，在濃煙漫天、轟隆作響的場景中，「虎爺轎」屹立不搖，轎夫無畏無懼的神采，讓群眾懾服。每當起馬炮放完，太子先鋒出發後，大夥最期待的就是虎爺吃炮了，其炮量一定會讓你驚訝不已！</p>
    <p>近期幾年在<b>金垂髫鄉土文史協會</b>的推廣之下，多數的吃炮方式慢慢的又回復成使用犁頭生搭配火爐的古早式犁炮，在三月十九的這幾天，都可以在媽祖廟廣場前、圓環等路口看到這種傳統的北港犁炮，而現場民眾也都有機會可以參與犁炮的活動喔！</p>

