    <h2>百年藝陣</h2>

    <p>在北港的百年陣頭中，其實有很多都是即將失傳的民俗藝陣，甚至有些已經解散。身為一個北港人，我想應該讓更多人看到屬於臺灣特有的本土民俗文化，歡迎大家將北港這一份特色<b>分享</b>出去吧！</p>
    <p>文化古鎮當然不僅限於朝天宮的文物與活動，舉凡北港圓環的顏思齊紀念碑、三級古蹟義民廟、知名景點甕牆、美食小吃，這些都是北港的在地文化、特色。</p>
    <p>其實來到這古鎮，可以看到的不止是香客人潮、也不止是香紙鞭炮，如果細心品嘗這些人文藝術，其實可以看到先民開墾台灣的痕跡。如果要把每片磚瓦的故事講過一輪，我想那不是三言兩語的，想知道更多北港之美，歡迎各位親自到北港來玩吧！</p>
