      <li>繞境所經接到，如有樹木、車輛等障礙物，請鎮公所、各里辦公處及警察單位協助宣導清除，以保持路線之暢通。</li>
      <li>請北港、朴子、民雄分局及所轄派出所，協助依本宮媽祖繞境路線，於五日前先做全面之交通整頓與疏導工作，以保持路線之暢通。</li>
      <li>繞境隊伍行鏡中，請警方配合依行程、地段、時間作局部交通管制，以維持交通順暢。</li>
      <li>請北港義勇警察分隊及北港義勇交通隊，協助繞境隊伍所經路線之交通疏導及管制任務。</li>
      <li>聖駕路經之處請各里辦公處、各單位協助鼓勵各里民、商家、住戶在門前恭設香案、鮮花、水果恭迎聖駕，以表誠敬。</li>
      <li>為推行環保觀念，減少噪音即空氣污染，在繞境沿途請各里辦公處、各單位協助宣導，請信徒減少燃放炮竹，如需然放炮竹，在購買時應選用合格廠牌，並符合國家訂定安全標準，同時嚴禁成推燃放爆竹，並禁止在神轎底下施放，以防發生意外及對媽祖不敬，聖駕經過後請各里戶自行清掃炮屑，並請北港鎮公所垃圾車協助清運，以維護街道整潔。</li>
